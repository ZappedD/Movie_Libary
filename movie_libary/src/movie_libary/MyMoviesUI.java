    private void jBtnRemoveMovieMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnRemoveMovieMousePressed
        // TODO add your handling code here:

        String title = jListMovies.getSelectedValue();
        System.out.println(jListMovies.getSelectedIndex());
        System.out.println(title);
        if (title != null) {

            int id = myMovies.getMovieID(title);

            ProductBean productBean = new ProductBean();
            try {
                productBean.removeMovie(id);
                myMovies.removeMovie(title);

            } catch (SQLException ex) {
                Logger.getLogger(MyMoviesUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Du måste välja en film att ta bort");
        }
        updateMyMovies();
    }//GEN-LAST:event_jBtnRemoveMovieMousePressed

    private void jListMoviesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListMoviesMousePressed
        // TODO add your handling code here:

        String title = jListMovies.getSelectedValue();
        String plot = myMovies.getMoviePlot(title);
        if (plot == "Not found") {
            JOptionPane.showMessageDialog(rootPane, "Some thing happend, what did you do? How did we come to this?");
        }
        int time = myMovies.getMovieTime(title);
        int rating = myMovies.getMovieRatingOf(title);
        jTextMPlot.setText(title + "\n" + plot);
        jLPlaytime.setText(String.valueOf(time) + " min");
        jLRating.setText("Rating " + rating);
    }//GEN-LAST:event_jListMoviesMousePressed

