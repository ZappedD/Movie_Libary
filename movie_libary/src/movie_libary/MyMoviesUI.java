    public void updateMyMovies() {
        ProductBean productBean = new ProductBean();

        try {
            myMovies = productBean.getMoviesFromDataBase();
        } catch (SQLException ex) {
            Logger.getLogger(MyMoviesUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        //Start of setign the list
        DefaultListModel listModel = new DefaultListModel();
        String toAdd = "";
        for (int i = 0; i < myMovies.getNrOfMovies(); i++) {
            toAdd = myMovies.getMovieTitleAt(i);
            listModel.addElement(toAdd);

        }
        System.out.println("Succes update Movie");
        //cealrar den gammla listan
        jListMovies.clearSelection();
        jListMovies.setModel(listModel);
        //End
    }

