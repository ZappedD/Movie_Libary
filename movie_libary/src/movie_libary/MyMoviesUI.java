    private void jBIMDbMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBIMDbMousePressed
        // TODO add your handling code here:
        if (jTtFLink.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Du måste fylla i en länk");
        } else {
            String link = jTtFLink.getText();
            ProductBean productbean = new ProductBean();
            try {
                productbean.postMovieScrape(link);

                updateMyMovies();
                jTabbedPaneMain.setSelectedIndex(0);
                jTtFLink.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(MyMoviesUI.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jBIMDbMousePressed

