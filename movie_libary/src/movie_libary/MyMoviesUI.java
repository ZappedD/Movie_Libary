    private void jBtnAddMovieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnAddMovieMouseClicked
        // TODO add your handling code here:
        if (jTtpAddTitle.getText().isEmpty() == false
                && jTtpMoviePlot.getText().isEmpty() == false
                && jTtpMovieTime.getText().isEmpty() == false) {
            System.out.println("add Movie");

            ProductBean productBean = new ProductBean();
            String title = jTtpAddTitle.getText();
            String plot = jTtpMoviePlot.getText();
            int time = Integer.parseInt(jTtpMovieTime.getText());
            int rating = jSpinnerMovieRating.getComponentCount();

            try {
                productBean.postMovie(title, plot, time, rating);
                updateMyMovies();        
                jTabbedPaneMain.setSelectedIndex(0);

            } catch (SQLException ex) {
                Logger.getLogger(MyMoviesUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, " DU måste fylla i alla fäten utom betyg");
        }
        updateMyMovies();
    }//GEN-LAST:event_jBtnAddMovieMouseClicked

